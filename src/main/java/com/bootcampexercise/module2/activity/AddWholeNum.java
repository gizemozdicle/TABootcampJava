package com.bootcampexercise.module2.activity;

public class AddWholeNum {



    public static void main(String[] args) {

        int sum=50;

        for (int i = 50; i < 100; i++) {

            sum=sum+1;

            System.out.println("Sum of numbers from 50 to 100 is " +sum);

        }


    }

}
